class Productos{
    cliente
    item
    descripcion
    precio
    cantidad
    fecha
    hora
    constructor(cliente,item,descripcion,precio,cantidad, fecha,hora){
        this.cliente = cliente;
        this.item = item;
        this.descripcion = descripcion;
        this.precio = precio;
        this.cantidad = cantidad;
        this.fecha = fecha;
        this.hora = hora;
    }

}